def get_section_image(
    section: str,
    event_id: str | None = None,
    prefer_mapsapi: bool = False,
) -> str:
    if prefer_mapsapi and event_id:
        return get_section_map_image(section, event_id)

    if section in MSG_KNICKS_SECTION_IMAGES:
        return MSG_KNICKS_SECTION_IMAGES[section]

    stripped = section.rstrip("WCDBS").rstrip("UL")
    if stripped in MSG_KNICKS_SECTION_IMAGES:
        return MSG_KNICKS_SECTION_IMAGES[stripped]

    if event_id:
        return get_section_map_image(section, event_id)

    return ""

def get_section_map_image(
    section: str,
    event_id: str,
    width: int = 600,
    highlight_color: str = "FF6633",
    bg_color: str = "1a1a1a",
) -> str:
    return (
        f"https://mapsapi.tmol.io/maps/geometry/3/event/{event_id}/image"
        f"?systemId=HOST"
        f"&sectionNames={section}"
        f"&pc={bg_color}"
        f"&sectionLevel=true"
        f"&shc={highlight_color}"
        f"&w={width}"
        f"&margin=20"
        f"&placeId="
        f"&app=PRD2663_EDP_NA"
    )

def get_venue_thumbnail(event_id: str, width: int = 400) -> str:
    return (
        f"https://mapsapi.tmol.io/maps/geometry/3/event/{event_id}/image"
        f"?systemId=HOST"
        f"&sectionLevel=true"
        f"&pc=1a1a1a"
        f"&w={width}"
        f"&margin=15"
    )

def get_level_name(section: str) -> str:
    sec = section.strip()
    if sec.isdigit():
        num = int(sec)
        if num <= 12:
            return "🏀 Courtside"
        elif num <= 120:
            return "🔵 100 Level (Lower Bowl)"
        elif num <= 227:
            return "🟡 200 Level (Chase Bridge / Club)"
        elif num <= 328:
            return "🟢 300 Level (Upper Bowl)"
        elif num <= 421:
            return "⚪ 400 Level (Terrace)"
    if "VIP" in sec.upper():
        return "⭐ VIP"
    if "WC" in sec.upper():
        return "♿ Wheelchair Accessible"
    return "🎟️ General"
